use {
    datastore::Datastore,
    futures::{future, StreamExt},
    std::net::{IpAddr, Ipv6Addr, SocketAddr},
    tarpc::{
        context,
        server::{self, incoming::Incoming, Channel},
        tokio_serde::formats::Bincode,
    },
};

mod config;

// This is the type that implements the generated World trait. It is the
// business logic and is used to start the server.
#[derive(Clone)]
struct HelloServer(SocketAddr);

#[tarpc::server]
impl Datastore for HelloServer {
    async fn get(self, _: context::Context, key: String) -> String {
        tracing::info!("Received request for key: {}", key);
        "value".into()
    }

    async fn set(self, _: context::Context, key: String, value: String) -> Option<String> {
        tracing::info!("Received request to set key: {} to value: {}", key, value);
        None
    }
}

#[tokio::main]
async fn main() -> anyhow::Result<()> {
    color_backtrace::install();
    config::init()?;
    common::utils::logging::init(&crate::config::get().logging_directive)?;

    let socket_address = (IpAddr::V6(Ipv6Addr::LOCALHOST), 2507);
    let listener = tarpc::serde_transport::tcp::listen(&socket_address, Bincode::default).await?;
    tracing::info!("Listening on {}", listener.local_addr());
    tracing::info!("Listening on port {}", listener.local_addr().port());

    listener
        // Ignore accept errors.
        .filter_map(|r| future::ready(r.ok()))
        .map(server::BaseChannel::with_defaults)
        // Limit channels to 1 per IP.
        .max_channels_per_key(1, |t| t.transport().peer_addr().unwrap().ip())
        // serve is generated by the service attribute. It takes as input any type implementing
        // the generated World trait.
        .map(|channel| {
            let server = HelloServer(channel.transport().peer_addr().unwrap());
            channel.execute(server.serve())
        })
        // Max 10 channels.
        .buffer_unordered(10)
        .for_each(|_| async {})
        .await;

    Ok(())
}
